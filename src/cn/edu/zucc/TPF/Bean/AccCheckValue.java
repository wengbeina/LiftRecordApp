package cn.edu.zucc.TPF.Bean;

public class AccCheckValue {
    private float accXCheck = 0;
    private float accYCheck = 0;
    private float accZCheck = 0;
    private boolean pause = false;
	public float getAccXCheck() {
		return accXCheck;
	}
	public void setAccXCheck(float accXCheck) {
		this.accXCheck = accXCheck;
	}
	public float getAccYCheck() {
		return accYCheck;
	}
	public void setAccYCheck(float accYCheck) {
		this.accYCheck = accYCheck;
	}
	public float getAccZCheck() {
		return accZCheck;
	}
	public void setAccZCheck(float accZCheck) {
		this.accZCheck = accZCheck;
	}
	public boolean isPause() {
		return pause;
	}
	public void setPause(boolean pause) {
		this.pause = pause;
	}
	

}
